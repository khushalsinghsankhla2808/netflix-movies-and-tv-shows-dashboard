
--CREATING TABLE NETFLIX
DROP TABLE IF EXISTS netflix;
CREATE TABLE NETFLIX (
	SHOW_ID VARCHAR(20),
	TYPE VARCHAR(10),
	TITLE VARCHAR(500),
	DIRECTOR VARCHAR(500),
	CASTS VARCHAR(1000),
	COUNTRY VARCHAR(500),
	DATE_ADDED VARCHAR(50),
	RELEASE_YEAR INT,
	RATING VARCHAR(10),
	DURATION VARCHAR(15),
	LISTED_IN VARCHAR(500),
	DESCRIPTION VARCHAR(300)
);


