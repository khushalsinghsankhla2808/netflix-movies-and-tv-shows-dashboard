--14. FIND 10 ACTORS WHO HAVE APPEARED IN THE HIGHEST NUMBER OF MOVIES PRODUCED IN INDIA.

SELECT
	TRIM(UNNEST(STRING_TO_ARRAY(CASTS, ','))) AS ACTORS,
	COUNT(*) AS TOTAL_COUNT
FROM
	NETFLIX
WHERE
	TYPE = 'Movie'
	AND COUNTRY ILIKE '%India%'
GROUP BY
	1
ORDER BY
	2 DESC
LIMIT
	10;

--15. CATEGORIZED THE CONTENT BASED ON THE PRESENCE OF THE KEYWORDS 'KILL'  AND 'VIOLENCE ' IN THE DESCRIPTION FEILD.
-- LABEL CONTENT CONTAINNING THESE KEYWORDS AS 'BAD' AND ALL OTHER CONTENT AS 'GOOD', COUNT HOW MANY ITEMS FALL INTO EACH CATEGORY

WITH
	NEW_TABLE AS (
		SELECT
			*,
			CASE
				WHEN DESCRIPTION ILIKE '%Kill%'
				OR DESCRIPTION ILIKE '%Violence%' THEN 'BAD_CONTENT'
				ELSE 'GOOD_CONTENT'
			END CATEGORY
		FROM
			NETFLIX
	)
SELECT
	CATEGORY,
	COUNT(*) AS TOTAL_COUNT
FROM
	NEW_TABLE
GROUP BY
	1;









