-- 15 BUISNESS PROBLEMS 

-- 1. COUNT THE NUMBER OF MOVIES VS TV SHOWS
SELECT
	TYPE,
	COUNT(*) AS TOTAL_COUNT
FROM
	NETFLIX
GROUP BY
	TYPE;



-- 2. FIND THE MOST COMMOM RATING FOR THE MOVIES AND TV SHOWS 

SELECT
	TYPE,
	RATING,
	TOTAL,
	RANKING
FROM
	(
		SELECT
			TYPE,
			RATING,
			COUNT(*) AS TOTAL,
			RANK() OVER (
				PARTITION BY
					TYPE
				ORDER BY
					COUNT(*) DESC
			) AS RANKING
		FROM
			NETFLIX
		GROUP BY
			TYPE,
			RATING
	) T
WHERE
	RANKING = 1;


-- 3. LIST ALL MOVIES RELEASED IN A SPECIFIC YEAR.. eg-2020

SELECT
	*
FROM
	NETFLIX
WHERE
	TYPE = 'Movie'
	AND RELEASE_YEAR = 2020;

-- 4. FIND THE TOP 5 CONTRIES WITH THE MOST CONTENT ON NETFLIX

SELECT
	TRIM(UNNEST(STRING_TO_ARRAY(COUNTRY, ','))) AS NEW_COUNTRY,
	COUNT(SHOW_ID) AS TOTAL_COUNT
FROM
	NETFLIX
WHERE
	COUNTRY IS NOT NULL
GROUP BY
	1
ORDER BY
	2 DESC
LIMIT
	5;

-- 5. IDENTIFY THE LONGEST MOVIE

SELECT
	*
FROM
	NETFLIX
WHERE
	TYPE = 'Movie'
	AND DURATION = (
		SELECT
			MAX(DURATION)
		FROM
			NETFLIX
	);

-- 6. FIND THE CONTENT ADDED IN THE LAST 5 YEARS

SELECT
	TYPE,
	DATE_ADDED,
	COUNT(*) AS TOTAL
FROM
	NETFLIX
WHERE
	DATE_ADDED IS NOT NULL
	AND TO_DATE(DATE_ADDED, 'Month DD, YYYY') >= CURRENT_DATE - INTERVAL '5 years'
GROUP BY
	TYPE,
	DATE_ADDED;

--7. FIND THE ALL THE MOVIES AND TV SHOWS DIRECTED BY THE RAJIV CHILAKA

SELECT
	*
FROM
	NETFLIX
WHERE
	DIRECTOR ILIKE '%Rajiv Chilaka%';

-- 8. LIST ALL TV SHOWS WITH MORE THAN 5 SEASONS
SELECT
	*
FROM
	NETFLIX
WHERE
	TYPE = 'TV Show'
	AND SPLIT_PART(DURATION, ' ', 1)::NUMERIC > 5;

-- 9. COUNT THE NUMBER OF CONTENT ITEM IN EACH GERNE

SELECT
	TRIM(UNNEST(STRING_TO_ARRAY(listed_in, ','))) AS GERNE,
	COUNT(SHOW_ID) AS TOTAL_COUNT
FROM
	NETFLIX
WHERE
	LISTED_IN IS NOT NULL
GROUP BY
	1;

-- 10. FIND EACH YEAR AND THE AVERAGE NUMBER OF CONTENT RELEASE BY INDIAON NETFLIX .
--		TOP 5 YEAR WITH HIGHEST AVG CONTENT RELEASE 

SELECT
	EXTRACT(YEAR FROM TO_DATE(date_added, 'Month DD ,YYYY')) AS year,
	COUNT(*) AS yearly_count,
	ROUND(COUNT(*)::NUMERIC/(SELECT COUNT(*) FROM netflix WHERE country = 'India')::numeric * 100,2) AS AVG_NO_OF_CONTENT_RELEASED
FROM netflix
WHERE country = 'India'
GROUP BY 1;


-- 11. LIST ALL THE MOVIES WHICH ARE DOCUMENTARIES 


SELECT
	TITLE,
	TYPE,
	GERNE
FROM
	(
		SELECT
			TITLE,
			TYPE,
			TRIM(UNNEST(STRING_TO_ARRAY(LISTED_IN, ','))) AS GERNE
		FROM
			NETFLIX
	) T
WHERE
	TYPE = 'Movie'
	AND GERNE = 'Documentaries';


